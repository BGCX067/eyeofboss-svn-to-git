/**
 * Zmienia czas w sekundach na stringa
 */
private String longToTimePeriod(long time){
    String czas=new String("");
    long temp=time/3600;
    czas=Long.toString(temp) + "h:";
    time%=3600;
    temp=time/60;
    czas+=Long.toString(temp)+"m:";
    time%=60;
    temp=time/60;
    czas+=Long.toString(temp)+"s";
    return czas;
}

